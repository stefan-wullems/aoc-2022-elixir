defmodule AdventOfCode.Day02 do
  @rules %{
    rock: %{
      score: 1,
      wins_against: :scissors,
      loses_against: :paper
    },
    paper: %{
      score: 2,
      wins_against: :rock,
      loses_against: :scissors
    },
    scissors: %{
      score: 3,
      wins_against: :paper,
      loses_against: :rock
    }
  }

  defp hand_score(my_hand) do
    get_in(@rules, [my_hand, :score])
  end

  defp round_score(opponent_hand, my_hand) do
    cond do
      get_in(@rules, [opponent_hand, :wins_against]) == my_hand ->
        0

      my_hand == opponent_hand ->
        3

      get_in(@rules, [my_hand, :wins_against]) == opponent_hand ->
        6
    end
  end

  defp calc_score(opponent_hand, my_hand) do
    hand_score(my_hand) + round_score(opponent_hand, my_hand)
  end

  defp infer_my_hand(opponent_hand, result) do
    case result do
      :win ->
        get_in(@rules, [opponent_hand, :loses_against])

      :draw ->
        opponent_hand

      :lose ->
        get_in(@rules, [opponent_hand, :wins_against])
    end
  end

  def part1(_args) do
    AdventOfCode.Day02.Input.parsed_input_part1()
    |> Enum.map(fn {opponent_hand, my_hand} -> calc_score(opponent_hand, my_hand) end)
    |> Enum.sum()
  end

  def part2(_args) do
    AdventOfCode.Day02.Input.parsed_input_part2()
    |> Enum.map(fn {opponent_hand, desired_result} ->
      calc_score(opponent_hand, infer_my_hand(opponent_hand, desired_result))
    end)
    |> Enum.sum()
  end
end

